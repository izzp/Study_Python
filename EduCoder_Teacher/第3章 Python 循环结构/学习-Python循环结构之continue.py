"""
任务：计算给定列表中所有正数的和。
"""

list_num = [-5, 0, 5, 8, 101, -16, -15, 56, 20, -32, 20, 0, -19, 50, 100, 205, 302, 1901, 302]

# 请在下面的Begin-End之间按照注释中给出的提示编写正确的代码
##########Begin##########
# 使用continue语句计算给定列表中所有正数的和，按照输出格式打印结果
sum=0
for i in list_num:
    if i < 0:
        continue
    sum += i
print("正数和为%d" % sum)
##########End##########
